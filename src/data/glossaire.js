export function searchGlossaire(query) {
    if (!query || query.trim() === '') return glossaire
    const q = query.toLowerCase().trim()
    return glossaire.filter(item =>
        item.terme.toLowerCase().includes(q) ||
        item.termeFr.toLowerCase().includes(q) ||
        item.definition.toLowerCase().includes(q) ||
        item.categorie.toLowerCase().includes(q)
    )
}

